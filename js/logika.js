let a = true;
let b = false;
let c = true;
let d = false;

// OR akan menjadi true jika salah satu pembanding bernilai true
console.log(a || b); 
// AND akan bernilai false jika salah satu pembanding bernilai false
console.log(a && b);

let x = 8;
let y = 7;

// Lebih besar
console.log(x > y);
// Pembanding diatas akan bernilai "true", karena variabel 'x' memang bernilai lebih besar daripada variabel 'y'

// Lebih kecil
console.log(x < y);
// Pembanding diatas akan bernilai "false", karena variabel 'x' tidak bernilai lebih kecil daripada variabel 'y'

// Sama dengan
console.log(x == y);
// Pembanding diatas akan bernilai "false", karena variabel 'x' memang bernilai sama daripada variabel 'y'

// Lebih besar sama dengan
console.log(x >= y);
// Pembanding diatas akan bernilai "true", karena variabel 'x' memang bernilai lebih besar sama dengan daripada variabel 'y'

// Lebih kecil sama dengan
console.log(x <= y);
// Pembanding diatas akan bernilai "false", karena variabel 'x' tidak bernilai lebih besar daripada variabel 'y'

// Tidak sama dengan
console.log(x != y);
// Pembanding diatas akan bernilai "true", karena variabel 'x' tidak bernilai sama dengan variabel 'y'

